def build_cjk_pattern():
    """Build a regex pattern that matches Chinese characters in order."""
    # Sort by length descending to match longer phrases first
    all_terms = list(CJK_MAP.keys()) + list(SINGLE_CJK_FALLBACK.keys())
    all_terms.sort(key=len, reverse=True)
    # Escape for regex
    escaped = [re.escape(t) for t in all_terms]
    return re.compile('|'.join(escaped))

def replace_cjk(text, pattern, mapping):
    """Replace CJK characters in text using the mapping."""
    def replacer(match):
        matched = match.group(0)
        return mapping.get(matched, matched)
    return pattern.sub(replacer, text)

